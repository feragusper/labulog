export type Lang = "es" | "en";

const KEY = "labulog_lang";

function detect(): Lang {
  const stored = localStorage.getItem(KEY) as Lang | null;
  if (stored) return stored;
  return navigator.language.toLowerCase().startsWith("es") ? "es" : "en";
}

